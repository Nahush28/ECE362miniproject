//outputs to the display
void color_in(uint8_t RGB){
    GPIOB->BSRR |= (RGB & 0x01) == 0x01? 1<<11: 1<<(11+16);
    GPIOB->BSRR |= (RGB & 0x02) == 0x02? 1<<13: 1<<(13+16);
    GPIOB->BSRR |= (RGB & 0x04) == 0x04? 1<<9: 1<<(9+16);

    GPIOB->BSRR |= (RGB & 0x10) == 0x10? 1<<10: 1<<(10+16);
    GPIOB->BSRR |= (RGB & 0x20) == 0x20? 1<<12: 1<<(12+16);
    GPIOB->BSRR |= (RGB & 0x40) == 0x40? 1<<8: 1<<(8+16);

}

//delay functions
void nano_wait(unsigned int n) {
    asm(    "        mov r0,%0\n"
            "repeat: sub r0,#83\n"
            "        bgt repeat\n" : : "r"(n) : "r0", "cc");
}
void small_delay(){
    nano_wait(1000);
}

//enables interrupt for PA2 (button pressing)
void enable_exti(void){
    RCC -> APB2ENR |= RCC_APB2ENR_SYSCFGCOMPEN;
    SYSCFG ->EXTICR[0] &= ~SYSCFG_EXTICR1_EXTI2;
    SYSCFG ->EXTICR[0] |=  SYSCFG_EXTICR1_EXTI2_PA;
    EXTI -> RTSR = EXTI_RTSR_TR2;
    EXTI -> IMR = EXTI_IMR_MR2;
    NVIC -> ISER[0] |= 1 << EXTI2_3_IRQn;
    NVIC_SetPriority(EXTI2_3_IRQn, 0);
}

//functions for clocking matrix
void gpio_clock(){
    GPIOA->BSRR |= 1<<8;
    small_delay();
    GPIOA->BRR |= 1<<8;
}
