//reverses bit order for 1d array
void translate_frame(uint8_t* frame) {
    for (int i = 0; i < 16; i++) {
        for (int j = 0; j < 64; j++) {
            uint8_t left = frame[i * 64 + j] & 0xf0;
            frame[i * 64 + j] = frame[i * 64 + j] << 4;
            frame[i * 64 + j] = frame[i * 64 + j] | (left >> 4);
        }
    }
}
